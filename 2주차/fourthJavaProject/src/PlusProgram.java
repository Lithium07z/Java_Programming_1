public class PlusProgram {
	public static void main(String[] args) {
		int x; // 첫 번째 정수
		int y; // 두 번째 정수 
		int sum; // 두 정수의 합을 저장하는 변수
		
		x = 100; 
		y = 200;
		sum = x + y;
		
		System.out.println("두 정수의 합은 " + sum + "입니다.");
	}

}
